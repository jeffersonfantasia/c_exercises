void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int temp;

	size = (size - 1);
	i = 0;
	j = 0;
	while (i < size)
	{
		while (j < size - i)
		{
			if (tab[j] > tab[j + 1])
			{
				temp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = temp;
			}
			j++;
		}
		j = 0;
		i++;
	}
}
